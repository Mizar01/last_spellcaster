# The Last Spellcaster (Work in progress)

##  Brief description:

This is a platform game with skill progression, combat, and environmental puzzle solving.

It is inspired by metroidvania games, wich I am a big fan of.

The player has four main spell it can learn and cast:
 - **fire**: the fire can burn enemies, but at further levels it can burn vines and environmental obstacles
 - **thunder**: used to damage enemies, but also to turn on/off special door switches
 - **ice**: used to transform enemies in ice cubes you can walk on
 - **wind**: use to push enemies in specific places, mostly useful in combination with ice to make some platforms to jump on a get over some gaps.

The player must use special **focus stones** to change the current spell to use.

To pass some special doors, or to learn spells, the player must collect **shards** 

## Features (some in development):
 - 9 tile maps (48x32) to explore. All the maps are connected in many ways 
 - Already visited maps remember open/closed doors, burnt vines or killed bosses.
 - 3 bosses
 - some npcs
 - 4 spells to learn and upgrade
 - 5/6 enemy types - killed enemies will drop shards
 - shards scattered around the world
 - doors and their remote switches
 - (if code optiomization will let me) - a minimap with player position

## Controls
  - left/right: move
  - z: jump
  - x: attack
  - enter/menu: access to some special option (still in progress)

